export function isLanguage(value: string): value is Language {
  return value === "en" || value === "bg";
}

export function t(language: Language, key: string, vars?: Record<string, string | number>) {
  const template = translations[language][key] ?? translations[DEFAULT_LANGUAGE][key] ?? key;

  if (!vars) {
    return template;
  }

  return Object.entries(vars).reduce((acc, [token, value]) => {
    return acc.replaceAll(`{{${token}}}`, String(value));
  }, template);
}
